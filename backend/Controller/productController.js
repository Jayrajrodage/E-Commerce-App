const updateProductSchema = z.object({
  name: z.string().max(30),
  description: z.string().min(10).max(300),
  price: z.number().min(20).max(70),
  category: z.string().max(30),
  quantity: z.number(),
  shipping: z.boolean(),
});
const updateProductController = async (req, res) => {
  try {
    const parsedData = updateProductSchema.safeParse(req.fields);
    if (!parsedData.success) {
      return res.status(200).json({ message: parsedData.error });
    }
    let name = parsedData.data.name;
    const { photo } = req.files;
    if (photo && photo.size > 2000000) {
      return res.status(500).send({
        success: false,
        message: "Photo Reqired and should less than 2Mb",
      });
    }

    const products = await productModel.findByIdAndUpdate(
      req.params.pid,
      { ...req.fields, slug: slugify(name) },
      { new: true }
    );
    if (photo) {
      products.photo.data = fs.readFileSync(photo.path);
      products.photo.contentType = photo.type;
    }
    await products.save();
    res.status(201).send({
      success: true,
      message: "Product Updated Successfully",
      products,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error in Updte product",
    });
  }
};

