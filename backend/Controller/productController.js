//payment gateway
var gateway = new braintree.BraintreeGateway({
  environment: braintree.Environment.Sandbox,
  merchantId: process.env.BRAINTREE_MERCHENT_ID,
  publicKey: process.env.BRAINTREE_PUBLIC_KEY,
  privateKey: process.env.BRAINTREE_PRIVATE_KEY,
});

const createProductSchema = z.object({
  name: z.string().max(100),
  description: z.string().min(10).max(300),
  price: z.string().max(70),
  category: z.string().max(30),
  quantity: z.string(),
});

const createProductController = async (req, res) => {
  try {
    const parsedData = createProductSchema.safeParse(req.fields);
    if (!parsedData.success) {
      const errormessages = parsedData.error.issues.map((obj) => obj.message);
      return res.status(200).send({ message: errormessages });
    }
    let name = parsedData.data.name;

    const { photo } = req.files;
    if (photo && photo.size > 2000000) {
      return res.status(202).send({
        success: false,
        message: "Photo Reqired and should less than 2Mb",
      });
    }

    const products = new productModel({ ...req.fields, slug: slugify(name) });
    if (photo) {
      products.photo.data = fs.readFileSync(photo.path);
      products.photo.contentType = photo.type;
    }
    await products.save();
    res.status(201).send({
      success: true,
      message: "Product Created Successfully",
      products,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error in crearing product",
    });
  }
};

